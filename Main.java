import java.util.Scanner;
public class Main{
    public static void main(String []arg){
        Scanner sc=new Scanner(System.in);
        int n=sc.nextInt();
        int a[]=new int[n];
        int b[]=new int[10];
        int k=0;
        for(int i=0;i<n;i++){
            a[i]=sc.nextInt();
        }
        int sum=0;
        for(int i=n-1;i>=0;i--){
           if(a[i]>sum){
            b[k++]=a[i];
           }
           sum=sum+a[i];

        }
        for(int i=k-1;i>=0;i--){
            System.out.print(b[i]+" ");
        }
    }
}

